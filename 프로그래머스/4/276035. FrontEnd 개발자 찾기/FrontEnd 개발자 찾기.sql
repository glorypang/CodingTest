SELECT DISTINCT(A.ID),A.EMAIL,	A.FIRST_NAME,	A.LAST_NAME
FROM DEVELOPERS A JOIN SKILLCODES B ON A.SKILL_CODE & B.CODE = B.CODE
WHERE B.CATEGORY = 'Front End'
ORDER BY 1